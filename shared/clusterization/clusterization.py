"""
Klasa odpowiedzialna za predykcjƒô klastra dla stacji rowerowych.
Wykorzystuje wytrenowany model K-Means do grupowania stacji
o podobnych charakterystykach operacyjnych i warunkach ≈õrodowiskowych.

Klasteryzowane cechy:
- Dane o stacji: 'bikes_available', 'docks_available', 'capacity',
    'manual_bikes_available', 'electric_bikes_available'.
    Okre≈õlajƒÖ bie≈ºƒÖcy stan i pojemno≈õƒá stacji, kluczowe dla zarzƒÖdzania flotƒÖ.
- Dane pogodowe: 'temperature', 'wind_kph', 'precip_mm', 'humidity',
    'weather_condition'.
    Pogoda znaczƒÖco wp≈Çywa na u≈ºytkowanie rower√≥w, pozwalajƒÖc na grupowanie
    stacji wed≈Çug ich reakcji na r√≥≈ºne warunki atmosferyczne.
- Dane o jako≈õci powietrza: 'fine_particles_pm2_5', 'coarse_particles_pm10'.
    Zanieczyszczenie powietrza mo≈ºe wp≈Çywaƒá na decyzje u≈ºytkownik√≥w,
    zmieniajƒÖc wzorce korzystania ze stacji.

Celem jest identyfikacja typowych zachowa≈Ñ stacji w r√≥≈ºnych warunkach,
co wspiera optymalizacjƒô dystrybucji rower√≥w i dok√≥w.
"""

class BikeStationClusterPredictor:
    # Inicjalizuje predyktor z wczytanym modelem
    def __init__(self, model_path='/app/shared/clusterization/models/bikes_kmeans.pkl'):
        self.model_path = model_path
        self.model_name = os.path.basename(model_path).replace('.pkl', '') 
        self.new_model_path = model_path.replace('.pkl', '_new.pkl')
        self.model_data = None
        self.kmeans = None
        self.scaler = None
        self.label_encoder = None
        self.feature_names = None
        self.is_loaded = False
        
        # Przy inicjalizacji, je≈õli istnieje "_new" plik, zastƒÖp nim g≈Ç√≥wny
        if os.path.exists(self.new_model_path):
            save_log(self.model_name, "info", f"Znaleziono nowy model {self.new_model_path} przy starcie. Przenoszƒô go na g≈Ç√≥wnƒÖ ≈õcie≈ºkƒô.")
            try:
                if os.path.exists(self.model_path):
                    os.remove(self.model_path) # Usu≈Ñ stary model, je≈õli istnieje
                os.rename(self.new_model_path, self.model_path) # Przemianuj nowy na g≈Ç√≥wny
            except Exception as e:
                save_log(self.model_name, "error", f"B≈ÇƒÖd przy przenoszeniu {self.new_model_path} na {self.model_path} podczas startu: {e}")
        
        self.load_model()
    
    # Wczytuje zapisany model i wszystkie komponenty
    def load_model(self):
        try:
            if not os.path.exists(self.model_path):
                print(f"‚ö†Ô∏è Model nie istnieje: {self.model_path}")
                return False
                
            print(f"üîÑ ≈Åadowanie modelu z: {self.model_path}")
            self.model_data = joblib.load(self.model_path)
            
            self.kmeans = self.model_data['kmeans']
            self.scaler = self.model_data['scaler']
            self.label_encoder = self.model_data['label_encoder']
            self.feature_names = self.model_data['feature_names']
            
            print(f"‚úÖ Model za≈Çadowany pomy≈õlnie!")
            print(f"   Liczba klastr√≥w: {self.model_data['n_clusters']}")
            print(f"   Silhouette Score: {self.model_data['silhouette_score']:.3f}")
            save_log("cluster_bikes", "info", f"Za≈Çadowano model z {self.model_data['n_clusters']} klastr√≥w oraz {self.model_data['silhouette_score']:.3f} silhouette score.")

            self.is_loaded = True
            return True
            
        except Exception as e:
            print(f"‚ùå B≈ÇƒÖd ≈Çadowania modelu: {e}")
            save_log("cluster_bikes", "error", f"B≈ÇƒÖd ≈Çadowania modelu: {e}.")
            self.is_loaded = False
            return False

    # Przygotowuje cechy z pojedynczego dict'a (dla real-time predykcji)
    def prepare_features_from_dict(self, enriched_dict: Dict) -> Optional[pd.DataFrame]:
        
        # Mapowanie nazw kolumn z twojego enriched dict'a
        feature_mapping = {
            'bikes_available': 'bikes_available',
            'docks_available': 'docks_available', 
            'capacity': 'capacity',
            'manual_bikes_available': 'manual_bikes_available',
            'electric_bikes_available': 'electric_bikes_available',
            'temperature': 'temperature',
            'wind_kph': 'wind_kph',
            'precip_mm': 'precip_mm',
            'humidity': 'humidity',
            'weather_condition': 'weather_condition',
            'fine_particles_pm2_5': 'fine_particles_pm2_5',
            'coarse_particles_pm10': 'coarse_particles_pm10'
        }
        
        try:
            # Sprawd≈∫ czy wszystkie potrzebne klucze istniejƒÖ
            missing_keys = []
            feature_data = {}
            
            for model_key, dict_key in feature_mapping.items():
                if dict_key in enriched_dict:
                    feature_data[model_key] = enriched_dict[dict_key]
                else:
                    missing_keys.append(dict_key)
            
            if missing_keys:
                print(f"‚ö†Ô∏è Brakuje kluczy w danych: {missing_keys}")
                return None
            
            # Konweruj do DataFrame
            df = pd.DataFrame([feature_data])
            
            # Enkodowanie weather_condition z mapowaniem na 'unknown'
            try:
                df['weather_condition_encoded'] = self.label_encoder.transform(df['weather_condition'])
                df = df.drop('weather_condition', axis=1)
            except ValueError as e:
                original_weather = df['weather_condition'].iloc[0]
                print(f"‚ö†Ô∏è Nieznana warto≈õƒá weather_condition: '{original_weather}'")
                
                # Sprawd≈∫ czy 'unknown' istnieje w znanych klasach
                known_classes = list(self.label_encoder.classes_)
                if 'unknown' in known_classes:
                    print(f"üîÑ Mapujƒô '{original_weather}' -> 'unknown'")
                    df['weather_condition'] = 'unknown'
                    df['weather_condition_encoded'] = self.label_encoder.transform(df['weather_condition'])
                    df = df.drop('weather_condition', axis=1)
                else:
                    print(f"‚ùå Brak klasy 'unknown' w modelu. Znane klasy: {known_classes}")
                    # U≈ºyj pierwszej dostƒôpnej klasy jako fallback
                    fallback_weather = known_classes[0]
                    print(f"üîÑ U≈ºywam fallback: '{fallback_weather}'")
                    df['weather_condition'] = fallback_weather
                    df['weather_condition_encoded'] = self.label_encoder.transform(df['weather_condition'])
                    df = df.drop('weather_condition', axis=1)
            
            # Sprawd≈∫ czy sƒÖ NaN
            if df.isnull().any().any():
                print("‚ö†Ô∏è Dane zawierajƒÖ warto≈õci NaN")
                return None
            
            save_log("cluster_bikes", "info", f"Pomy≈õlnie przygotowano zmienne dla modelu z dicta.")
            return df
            
        except Exception as e:
            print(f"‚ùå B≈ÇƒÖd przygotowywania cech: {e}")
            save_log("cluster_bikes", "error", f"WystƒÖpi≈Ç b≈ÇƒÖd przy przygotowywaniu zmiennych dla modelu z dicta: {e}.")
            return None
    
    # Przewiduje klaster dla pojedynczego dict'a danych
    def predict_cluster_from_dict(self, enriched_dict: Dict) -> Optional[int]:
        
        if not self.is_loaded:
            print("‚ö†Ô∏è Model nie jest za≈Çadowany")
            return None
        
        # Przygotuj cechy
        features_df = self.prepare_features_from_dict(enriched_dict)
        
        if features_df is None:
            print("‚ö†Ô∏è Nie mo≈ºna przygotowaƒá cech")
            return None
        
        try:
            # Normalizacja
            features_scaled = self.scaler.transform(features_df)
            
            # Predykcja
            cluster_prediction = self.kmeans.predict(features_scaled)
            
            save_log("cluster_bikes", "info", f"Pomy≈õlnie przewidziano klaster dla danych.")
            return int(cluster_prediction[0])
            
        except Exception as e:
            save_log("cluster_bikes", "error", f"WystƒÖpi≈Ç b≈ÇƒÖd przy przewidywaniu klastra dla danych: {e}.")
            print(f"‚ùå B≈ÇƒÖd predykcji klastra: {e}")
            return None
    
    # Zwraca informacje o centrum klastra
    def get_cluster_info(self, cluster_id: int) -> Optional[Dict]:
        if not self.is_loaded or cluster_id >= len(self.kmeans.cluster_centers_):
            return None
        
        center = self.kmeans.cluster_centers_[cluster_id]
        feature_names = self.feature_names or [f"feature_{i}" for i in range(len(center))]
        
        cluster_info = {}
        for feature, value in zip(feature_names, center):
            cluster_info[feature] = float(value)
        
        return cluster_info
    
    # Prze≈Çadowuje model z dysku
    def reload_model(self) -> bool:
        save_log(self.model_name, "info", f"üîÑ Rozpoczynam prze≈Çadowywanie modelu '{self.model_name}'...")
        print(f"üîÑ Prze≈Çadowywanie modelu '{self.model_name}'...")
        
        # Sprawd≈∫, czy nowy plik modelu istnieje
        if not os.path.exists(self.new_model_path):
            save_log(self.model_name, "warning", f"Brak nowego pliku modelu do prze≈Çadowania: {self.new_model_path}")
            print(f"‚ö†Ô∏è Brak nowego pliku modelu do prze≈Çadowania: {self.new_model_path}")
            return False

        try:
            # 1. Usu≈Ñ stary plik modelu (je≈õli istnieje), aby zrobiƒá miejsce na nowy
            if os.path.exists(self.model_path):
                os.remove(self.model_path)
                save_log(self.model_name, "info", f"Usuniƒôto stary plik modelu: {self.model_path}")
                print(f"Usuniƒôto stary plik modelu: {self.model_path}")
            
            # 2. Zmie≈Ñ nazwƒô nowego pliku na "g≈Ç√≥wny" plik modelu
            # Ta operacja jest atomowa na wiƒôkszo≈õci system√≥w plik√≥w.
            os.rename(self.new_model_path, self.model_path)
            save_log(self.model_name, "info", f"Zmieniono nazwƒô {self.new_model_path} na {self.model_path}.")
            print(f"Zmieniono nazwƒô {self.new_model_path} na {self.model_path}.")
            
            # 3. Za≈Çaduj nowo podmieniony model
            if self.load_model():
                save_log(self.model_name, "info", f"Model '{self.model_name}' pomy≈õlnie prze≈Çadowany.")
                print(f"‚úÖ Model '{self.model_name}' pomy≈õlnie prze≈Çadowany.")
                return True
            else:
                # Je≈õli ≈Çadowanie siƒô nie powiod≈Ço po podmianie, to jest problem
                save_log(self.model_name, "error", f"Nie uda≈Ço siƒô za≈Çadowaƒá nowo podmienionego modelu '{self.model_name}'.")
                print(f"‚ùå Nie uda≈Ço siƒô za≈Çadowaƒá nowo podmienionego modelu '{self.model_name}'.")
                return False
                
        except Exception as e:
            save_log(self.model_name, "error", f"B≈ÇƒÖd podczas atomowej podmiany modelu '{self.model_name}': {e}")
            print(f"‚ùå B≈ÇƒÖd podczas atomowej podmiany modelu '{self.model_name}': {e}")
            self.is_loaded = False # Upewnij siƒô, ≈ºe flaga jest False w przypadku b≈Çƒôdu
            return False

